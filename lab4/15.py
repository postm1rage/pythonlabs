fractal = [0, 2]

# вставляем в середину две ссылки на самого себя
fractal[1:1] = [fractal, fractal] # [1:1] - пустое место между элементами

print(fractal) # [0, [0, [...], 2], [0, [...], 2], 2]
print(fractal[1]) # [0, [0, [...], 2], [0, [...], 2], 2]
print(fractal[1] is fractal) # True - это один и тот же объектz